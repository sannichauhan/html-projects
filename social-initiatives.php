                        <h4 class="head-font20 wow fadeInUp clr_white">Soniya Berwa – the walk that changed her life!</h4>
                        <p class="wow fadeInUp">It was the time of COVID outbreak and the Shiksha Sambal team was in regular connect with 90% of their students either through online modes or through home visits and thus Shiksha Sambal team kept promoting education in all these 64 earmarked schools. Students were connected to the team and so was Soniya Berwa, girl studying in class 10<sup>th</sup> of Bheru Kheda school, Agucha Panchayat, Bhilwara District. But, the team observed that Soniya for some reason had got cut off with the Shiksha Sambal team and was not submitting her regular work or attending her online classes. After visiting her home the team got to know that Soniya’s father had passed away and that her mother, after Soniya’s fathers death, was concerned about her safety as it was a 4 kilometre walk every day for Soniya to go to her school and the economic condition of the family had also deteriorated. Hence, Soniya’s mother was no more willing to let her continue her studies and she being the eldest of the siblings expected her to engage in income generating activities. The Shiksha Sambal team, however, convinced Soniya’s mother of the need to educate her and persuaded other girls in the neighbourhood to accompany her to school. Regular interactions with the Shiksha Sambal team assured Soniya’s mother and led to Soniya taking interest in her studies and learn from the Shiksha Sambal team. Shiksha Sambal effectively plugged the distance between school and home for the young girl.</p>


