        <h6>GLOBAL DEMAND</h6>
        <p>Global refined consumption increased by 3.8% in CY 2021 reaching 13 Mt after declining to 12.5 Mt in CY 2020. Refined lead consumption is expected to reach 13.7 Mt by CY 2023. The Chinese and Indian
        automotive sectors are expected to be the main drivers of global demand along with other Asian countries.</p>
        <h6>PRODUCTION </h6>
        <p>Lead mined metal output (metal in concentrate) grew 5.8% in CY 2021 to 4.54 Mt after declining by 7.9% in CY 2020 due to pandemic. According to WoodMac, lead mined metal production is expected to grow at a CAGR of 1.4% during CY 2021-26 with lead mined metal production expected to reach 4.9 Mt in CY 2023. Production of secondary lead is expected to grow 2.9% in CY 2022 before normalising and attaining
        average growth of 1.4% during CY 2022-32.</p>
        <h6>INDIA DEMAND AND OPPORTUNITY</h6>
        <p>Lead consumption in India increased marginally from 1 Mt in CY 2020 to 1.11 Mt in CY 2021. Refined lead
        production increased from 1 Mt in CY 2020 to 1.14 Mt in CY 2021, of which 0.23 Mt was primary and 0.91 Mt secondary production.</p>

        <p>The country remains an attractive market for lead with demand likely to increase with an average growth of 6.4% until 2031, as estimated by WoodMac. The growth will be driven by the automotive sector and inverter battery market. Emerging opportunities like energy storage for electricity generated from photovoltaics (PV) will also drive demand, given Indiaâ€™s ambitious plan to aggressively expand solar PV capacity by 2030.</p>

        <p>Major domestic battery manufacturers continue to grow lead acid battery manufacturing capabilities on
          account of sustained demand. Apart from automotive, the industrial battery segment catering to data
        centres, financial institutions and telecom continue to witness strong growth in line with the digitalisation surge in the country.</p>
        <h6>PRICES </h6>
        <p>LME started with a price of US$ 1,896/ tonne in March 2021 and touched US$ 2,513/tonne in March 2022. Prices of lead have remained stagnant in a range US$ 2,100-2,500/tonne in FY 2021-22 and expected to remain within this range over the next financial year.</p>
        <h6>GLOBAL OUTLOOK</h6>
        <p>According to WoodMac, the global demand forecast for the medium term (2022-27) is estimated at 1.6% pa. This growth would be driven mainly by Asian countries. The average demand growth during 2022-32 is
        expected to be around 1.7%. Restocking of supply chains especially in North America and Europe along with pent up automotive demand, mostly from China, India and other Southeast Asian countries is likely to ensure sustained demand of lead.</p>

        <p>Despite rising development in new battery technologies, mainly for hybrid and battery electric vehicles
          designed to replace the internal combustion engine, the conventional demand for lead acid batteries is
        expected to sustain. This includes uses for powering ancillary systems such as engine monitoring, climate control, satellite navigation systems and to maintain stable voltages for vehicle management computers.</p>

        <p>A major portion of lead demand is likely to come from developing economies growing at an average of 3.1% until 2031. Rising vehicle production and penetration, infrastructure development and new telecom networks (ongoing 4G and upcoming 5G) will continue to support demand growth.</p>
