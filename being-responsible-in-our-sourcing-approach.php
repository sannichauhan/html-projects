  <h3 class="head-font20 wow fadeInUp mar-t30">BUSINESS PARTNER RISK MANAGEMENT</h3>
  <p class="wow fadeInUp">At Hindustan Zinc, we operate an exhaustive Business Partner monitoring and rationalisation system to ensure robust supply chain risk management. We ensure this through a combination of risk assessment and performance monitoring.</p> 

  <p class="wow fadeInUp">Business Partners are onboarded following detailed evaluation of qualification criteria. Those with issues in performance, in terms of quality and delivery, service KPIs or any other identified risk, are critically reviewed and monitored for performance improvement. In case of no improvement, their registration gets inactivated, and they are excluded from any further sourcing participation. </p>

  <p class="wow fadeInUp">Business Partners are also subjected to strict desktop-based due diligence along with at-site audits based on various parameters.</p> 

  <p class="wow fadeInUp">An adverse case of a business partner’s ethical non-compliance, major quality failure, fraudulent behaviour, false representation, contractual default, breach of safety norms, violation of contractual terms or code of conduct etc., may lead to cancellation of their registration. These business partners are subject to stringent quality management, capability assessments through site visits, as well as audits and performance reviews.</p>

