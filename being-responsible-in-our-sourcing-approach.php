          <h3 class="head-font20 wow fadeInUp clr_org">BUSINESS PARTNER IDENTIFICATION & ONBOARDING</h3>
          <p class="wow fadeInUp">Multiple methods, including market research & project-specific consulting engagement, Global Expression of Interest (EOI) on digital & print media, electronic marketplaces, online searches, direct approach, are used to identify potential business partners.</p>

          <p class="clr-blue-text">Transparent registration is done, through an online registration process on our SAP Ariba System. It involves filling up a detailed registration questionnaire form capturing key information, followed by evaluation and approval by designated authority. The questionnaire enables data collection on parameters like partner financial capabilities, quality, historical business performance, ESG, customer references and experiences.</p>

          <p class="wow fadeInUp">Smoother experience is facilitated on the Ariba platform through an Ariba Helpdesk Team, which provides 24x7 support to business partners while onboarding and usage of Ariba. Business Partners are classified as Critical Tier-I and Critical Tier-II, based on assessment of supply chain risk, Hindustan Zinc’s business dependency, and contract spend across supply and service categories.</p>
