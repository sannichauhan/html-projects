    <h4 class="head-font20 wow fadeInUp clr_white">Investing in Sustainable Development <br>
      FINDING ENVIRONMENT-FRIENDLY SUBSTITUTE FOR 
    GRAPHITE DISPERSANT</h4>

    <p class="wow fadeInUp">Hindustan Zinc team has been working on alternative products for graphite dispersant, which is a more sustainable product in terms of environment and health perspective. </p>
    <p class="wow fadeInUp">As part of the product development journey, a cross-functional team, consisting of members from procurement, process, and R&D functions, undertook the following:</p>

    <ul class="list-unstyled all-ul-li">
      <li class="wow fadeInUp">Conducted a research study for alternate reagents for the existing one, where a potential substitute was identified </li>
      <li class="wow fadeInUp">Held lab trials on the potential substitute at Hindustan Zinc’s NABL accredited research and development laboratory, which yielded promising results in terms of graphite carbon reduction and showed no adverse impact on metal recovery and grade </li>

    </ul>

    <p class="wow fadeInUp">Conducted plant trials twice over a period of 6 months </p>

    <ul class="list-unstyled all-ul-li">
      <li class="wow fadeInUp">Replaced existing reagent with the substitute after witnessing remarkable results of lab and plant trials</li>


    </ul>

    <p class="wow fadeInUp"><b>The Benefits</b></p>

    <p class="wow fadeInUp"><b>Operational excellence</b></p>
    <p class="wow fadeInUp">Relatively easy reagent preparation process</p>
    <p class="wow fadeInUp"><b>Graphite reduction </b></p>
    <ul class="list-unstyled all-ul-li">
      <li class="wow fadeInUp">Substitute has reduced the graphitic carbon in concentrate as compared with existing product by approximately 4%, which has significant benefit in terms of throughput in smelter operations </li>


    </ul>
    <p class="wow fadeInUp"><b>Cost impact </b></p>
    <p class="wow fadeInUp">The alternative product is cost-efficient on TCO basis. The benefit was two-fold and helped in savings on procurement as well as smelter operating costs.</p>
  </div>
  <h3 class="head-font20 wow fadeInUp clr_white mar-t40">CASE STUDY</h3>
  <div class="curly-border">

    <h4 class="head-font20 wow fadeInUp clr_white">Enabling Inventory Consolidation & Management <br>
    MIGRATING FROM INDIVIDUAL STORES TO MEGA WAREHOUSE</h4>

    <p class="wow fadeInUp">To get full visibility and control of the Company’s entire inventory, our supply chain team decided to consolidate the same at a single mega warehouse. The team felt the move would help bring in inventory optimisation, reduction in working capital, standardisation of spares, best-in-class storage facility, and a convenient use-and-pay model for seamless management of inventory.</p>

    <p class="wow fadeInUp">TVS Supply Chain Solutions is our warehouse partner, and a central warehouse spanning over 3.5 lacs sq ft has been set up near Udaipur airport. Currently, store migration from smelter is under last leg of execution, to be followed by mines’ store migration. Inbound deliveries from business partners to mega warehouse and outbound deliveries to site locations have been initiated. This project is also crucial for integration of Use & Pay model for spare part management.</p>

