  <li class="wow fadeInUp">Ensured continuous tracking and monitoring of the situations</li> 
  <li class="wow fadeInUp">Implemented alternative sourcing and usage of alternative products, while delivering operational efficiency </li>
  <li class="wow fadeInUp">Adequately stocked inputs at our plants and effectively managed working capital to serve both internal and external customers, delivering our best-ever performance </li>
  <li class="wow fadeInUp">Continuous communication with Business Partners to mitigate risks during the disruption in supply chain</li>
</ul>


