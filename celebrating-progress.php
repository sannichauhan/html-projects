            <h3 class="head-font20 wow fadeInUp mar-t30">CIRCULAR ECONOMY – A KEY STRATEGIC PRIORITY</h3>
            <p class="wow fadeInUp">The circular economy entails elimination of waste throughout our entire value chain and works to establish a net-positive system to ultimately protect the Earth’s finite supply of natural resources. As a leading organisation, we promote responsible production of materials. We comply with all regulatory requirements and have the necessary permits for treating and recycling waste at our operation site. </p>
            <p class="wow fadeInUp">Our waste to wealth community is continually working on optimising use of raw materials and exploring ways to reuse/ recycle the waste products created during the manufacturing process. It regularly monitors and reviews the progress on this front and formulates relevant strategies to achieve <b>‘Increase in 3x gainful utilisation of smelting process waste’</b> – one of the Company’s Sustainability Goals 2025. </p>
            <p class="wow fadeInUp">During the year, we took several innovative initiatives to steer the shift from traditional manufacturing approach of ‘produce-use-dispose’ to a more efficient, sustainable, and environment-friendly methodology.</p>
            <p class="wow fadeInUp">These include:</p>
