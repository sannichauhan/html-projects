                    <div class="info-slides">
                      <h3 class="head-font20 wow fadeInUp clr_sblue">RO REJECT SALTS</h3>
                      <p class="wow fadeInUp">The Sodium Sulphate Recovery Plant at Dariba Smelting Complex is operational and the quality of salt generated is suitable for jarosite precipitation, a procedure for removal of iron and lead impurities during zinc extraction. This has reduced hazardous mixed salt generation by <span class="middle-al">~</span>10%. </p>

                      <p class="wow fadeInUp">We conducted a detailed analysis of RO rejects mixed salt generated at Debari and Dariba smelter. Mixed salts generated from Dariba Zinc smelter, were found suitable for use by the soap industry, targeting utilisation of 1,500 tonnes of mixed salt in the manufacture of soap and detergent instead of being disposed in a Secured Landfill Facility (SLF).</p>
                    </div>
                  </div>
                  <div class="item">
                    <div class="info-slides">
                      <h3 class="head-font20 wow fadeInUp clr_sblue">DISRUPTIVE TECHNOLOGIES</h3>
                      <p class="wow fadeInUp">We are working in collaboration with various national and international institutes and technology providers for metal recovery and utilisation of tailing and jarosite. There are ongoing laboratory trials to evaluate the techno-economic feasibility of metal recovery from Rampura Agucha Mine tailing and jarosite/jarofix generated in our smelters.</p>
                    </div>
                  </div>
                  <div class="item">
                    <div class="info-slides">
                      <h3 class="head-font20 wow fadeInUp clr_sblue">JAROSITE UTILISATION</h3>
                      <p class="wow fadeInUp">During the year, 93,778 tonnes of Jarosite has been gainfully utilised in cement manufacturing. To further enhance Jarosite utilisation in cement manufacturing, we have been able to control the moisture content in Jarosite by optimum use of reagents, with this successful trial. We are targeting nearly 2 lac tonnes of Jarosite utilisation in FY 2022-23.</p>
                    </div>
                  </div>
                  <div class="item">
                    <div class="info-slides">
                      <h3 class="head-font20 wow fadeInUp clr_sblue">WAY FORWARD FOR JAROFIX UTILISATION</h3>
                      <p class="wow fadeInUp">The Company has been granted permission by Rajasthan State Pollution Control Board for gainful utilisation of jarofix waste in the construction of roads and highways, including embankments. We have partnered with some government agencies like NCCBM, NEERI and CRRI for the utilisation of jarosite in cement and road construction.</p>
                    </div>
                  </div>
                  <div class="item">
                    <div class="info-slides">
                      <h3 class="head-font20 wow fadeInUp clr_sblue">R&D, DIGITISATION AND CIRCULAR ECONOMY</h3>
                      <p class="wow fadeInUp">Our Research and Development (R&D) capabilities are playing an important role in propelling our circular economy goals. We are exploring various technologies for metal recovery from various residues and waste. R&D has taken up the challenge to increase metal recoveries and improve the process efficiency by implementing geo-metallurgy at mills and tracking of losses in tailing through an automated minerology setup. On smelting R&D is developing sustainable technologies in-house and in collaboration with institutes and technology providers. </p>

                      <p class="wow fadeInUp">The Company has been able to improve its metal recovery from beneficiation plants through digitalisation.</p>
