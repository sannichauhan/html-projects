          <h3 class="head-font20 wow fadeInUp mar-t30">MANAGING TAILINGS DAMS EFFECTIVELY</h3>
          <p class="wow fadeInUp">Responsibly managing waste from mining operations is crucial to ensure business sustainability. Any failure in tailing management can result in disaster, and hence, it is essential to communicate to stakeholders’ details of our tailing storage facilities and how we manage them. Hindustan Zinc has implemented global leading practices in tailing dam management, and we are associating with renowned global experts to provide long-term monitoring and advice on the design, construction, and operation of our three tailing storage facilities. Overall good construction quality, along with upgradation of tailing slurry transport and discharge lines through installation of piezometers (and inclinometer) with automated data collection and web-based monitoring, make it world-class.</p>
          <h3 class="head-font20 wow fadeInUp clr_sblue">EXISTING TAILING DAMS AT THE MINES</h3>
          <p class="wow fadeInUp">We recognise the impact of mine tailing and tailing facilities on the environment, nearby communities, and other stakeholders. We are taking extensive measures for construction, operation, maintenance, and closure of facilities that mitigate the risk of tailing dam failure. We incorporate the best available technologies as guided by Vedanta’s Tailing Management Facility Standard. Wherever possible, we repurpose tailing materials and waste rock as backfill to stabilise our underground mining operations. Remaining tailing are then placed in a specially designed tailing storage to minimise the environmental, social, and economic risks.</p>
          <h3 class="head-font20 wow fadeInUp clr_sblue">TAILING MANAGEMENT PLAN</h3>
          <p class="wow fadeInUp">Tailing management plans are an essential prerequisite for sound storage practice, as most failures of tailing storages around the world result from inadequate management of the storages. Effective implementation of a management plan not only results in a safer tailing storage facility (TSF) but will frequently reduce overall costs associated with operation and closure of the facility. </p>
          <p class="wow fadeInUp">All our operating mines have a Tailing Management Plan – an Operations, Maintenance and Surveillance (OMS) manual. It describes</p>
          <ul class="list-unstyled all-ul-li">
            <li class="wow fadeInUp">Roles and responsibilities of TSF Committee (Governing Tailing Management) </li>

            <li class="wow fadeInUp">Engineering and design of the tailing facilities</li> 

            <li class="wow fadeInUp">Procedures and processes for operation and maintenance, construction, surveillance of the facility, monitoring of the components and their parameters</li> 

            <li class="wow fadeInUp">Emergency procedures</li>
          </ul>
