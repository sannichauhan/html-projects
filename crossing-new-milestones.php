                <p class="wow fadeInUp">We are committed to zero liquid discharge across our locations. During the year, the Debari zinc smelter successfully commissioned a 3,000 kilolitre per day (KLD) zero liquid discharge ZLD plant. The move is aligned with our vision of ‘Zero Harm – Zero Waste – Zero Discharge’. We are striving to develop sustainable social solutions for a greener future by strengthening our zero discharge.</p> 

