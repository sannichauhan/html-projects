            <h3 class="head-font20 wow fadeInUp">OUR APPROACH TO HUMAN RIGHTS</h3>
            <p class="wow fadeInUp">With external scrutiny on human rights intensifying over the recent years, Hindustan Zinc continues to give utmost importance to upholding people’s fundamental rights while carrying out everyday business. We have zero tolerance for any form of forced and compulsory labour, child labour, slavery and human trafficking. We respect the rights of employees to associate freely and exercise collective bargaining. We are committed to being transparent in our disclosure on how we are addressing the human rights related challenges and opportunities concerning our workforce as well as local communities. Our Human Rights Policy is aligned with the United Nations Guiding Principles on Business and Human Rights and it covers all our employees, suppliers, contractors, joint ventures, and NGOs.</p>
            <h3 class="head-font20 wow fadeInUp">ELEMENTS OF HUMAN RIGHTS FRAMEWORK</h3>
            <h4 class="head-font18 wow fadeInUp clr_sblue">HUMAN RIGHTS</h4>
            <p class="wow fadeInUp">The Company ensures safe, suitable and sanitary work facilities for all its workers. Our policy on health and hygiene complies with national and international standards. It ensures that workers are provided with protective personal gear, equipment, and training necessary to perform their tasks safely at the workplace. We also ensure that the work week is limited to 48 hours, overtime is infrequent and limited, and workers are given reasonable breaks and rest periods.</p>
            <h4 class="head-font18 wow fadeInUp clr_sblue">SLAVERY AND HUMAN TRAFFICKING</h4>
            <p class="wow fadeInUp">Committed to the disclosure obligations under the UK Modern Slavery Act 2015, we ensure that modern day slavery, forced labour or servitude does not prevail in our business and supply chains. We strictly monitor all functions to ensure that our contractors, suppliers and business partners do not use any child, forced or trafficked labour.</p>
