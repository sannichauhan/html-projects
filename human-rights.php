            <h4 class="head-font18 wow fadeInUp clr_sblue">ENVIRONMENT</h4>
            <p class="wow fadeInUp">Our organisation supports a precautionary approach to environmental issues. This includes identification of any soil and water contamination at any of our sites, along with assessment of the environmental impact and remedies. We conduct systematic risk assessments of materials used, as well as the products and processes deployed, to apply the precautionary approach. We take measures to prevent and reduce the production of waste and ensure responsible waste management.</p>
