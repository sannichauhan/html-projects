        <h1>Journey of Mineral Resource
          Addition and Upgradation of
        Resources to Reserves</h1>
        <h5>Exploration<span class="pdtop10">Performance Overview</span></h5>
        <p class="wow fadeInUp ft20">Given the importance of mine life to our strategic growth agenda, our exploration teams remain consistently focussed on upgradation of our resources to reserves. We strive to replenish every tonne of the mined metal to ensure a sustained mine life of over 25 years at the current rate of metal production. Besides adopting new and innovative technologies, backed by our Zero Harm policy, we have put in place a robust
        exploration programme for adding mineral resource and delineation/upgradation of Resource to Reserves within our licence areas.</p>

        <h3><span>23</span></h3>
        <p class="wow fadeInUp max280">Exploration team strength
        (geologists, geophysicists, analysts)</p>
        <p>The team comprising of subject matter experts (SME) and field geologists is responsible for designing and executing exploration programmes with the support of outsourced service providers. The programmes
        actively embrace technology and innovation to propel the exploration journey.</p>
        <h6>IN FY 2021-22, IMPLEMENTATION OF MANY INNOVATIVE AND ADVANCED NEW 
        TECHNOLOGIES HELPED TO SCALE OUR EXPLORATION EFFORTS.</h6>
        <p><span>Establishing an in-house state-of-the-art hi-tech lab</span> at Zawar Mines in collaboration with our strategic
        business partner for sample preparation, analysis and report generation. The high-tech lab is equipped with a first-of its-kind Sample Handling system in India for acid mixing, normalisation, and dilution, thus ensuring zero human intervention. Equipped with a Microwave Digester, it will lead to a significant reduction in sample analysis cost and turnaround time.</p>
        <p><span>Enabled automation</span> of litho-structural data collection through Acoustic Televiewer (ATV)/ Optical Televiewer (OTV) tools of surface boreholes across all our sites. The data collection and analysis of geological structure (faults & folds) will support exploration for target generation, mine planning and geo-tech to locate and
          address shear zone problems ahead of mining development. The Gyro tool was also used for surveying and full wave sonic. Induced polarisation, resistivity, natural gamma, inductive conductivity, caliper, density,
        magnetic susceptibility and spectral gamma tools were used to develop petrophysical properties library of ore bodies and host sequences for all deposits of Hindustan Zinc. A pilot project was initiated on machine learning algorithms to predict the lithology and grade of mineralisation based on petrophysical properties.</p>
        <p><span>Geophysical assessment</span> was undertaken with Gravity and Magnetic survey of a new target area in Zawar mining lease. Integration of geochemical and geological inputs with this survey will lead to new exploration drilling targets.</p>
        <p><span>Synthetic modelling</span> and assessment of advanced technologies (passive seismic and mobile MT) was done with technology experts for deeper exploration in and around Rampura Agucha mining lease for discovery of new ore body.</p>
        <p><span>Mobile Carrier Rig (MCR)</span> modification was conducted in an innovative manner to enable drilling of shallow holes at surface location, where traditional surface drilling could not be undertaken due to technical
        constraints and unfavourable terrain conditions. The in-house innovation proved fruitful and the MCR rig was able to hit exploration targets successfully.</p>
        <p><span>Partnership with Windfall-Geotek, Canada,</span> was forged to leverage the latest technology based on Machine Learning and Artificial Intelligence, integrated with geological, geochemical and geophysical datasets to
        generate potential exploration targets within the licence areas.</p>
        <p>As in previous years, SRK Consulting (UK) Limited (“SRK”) has audited the Mineral Resources and Ore
        Reserves for Hindustan Zinc Limited (“HZL”), with an effective date of March 31, 2022. Presented below are the Mineral Resource and Ore Reserve statements, reported in accordance with the Australasian Code for the Reporting of Exploration Results, Mineral Resources and Ore Reserves, the JORC Code, 2012 Edition (“JORC”).</p>
        <h6>ORE RESERVES</h6>
        <p>As on March 31, 2022, Hindustan Zinc’s Ore Reserves are estimated to be 161.21 Mt of material, grading 5.9% Zn, 1.5% Pb and 58g/t Ag, which contains 9.57 Mt Zn, 2.45 Mt Pb and 298.4 Moz Ag.</p>
        <h6>MINERAL RESOURCES</h6>
        <p>As on March 31, 2022, Hindustan Zinc’s Exclusive Mineral Resources are estimated to be 286.73 Mt of
        material, grading 4.6% Zn, 2.0% Pb and 63g/t Ag, which contains 13.18 Mt Zn, 5.86 Mt Pb and 576.3 Moz Ag.</p>
        <p>At current mining rates, the R&R underpins metal production for more than 25 years.</p>
        <h4>With an overall brownfield exploration success rate of 91%, the team successfully added 25 Mt of gross addition to mineral resources as part of Hindustan Zinc’s R&R inventory during FY 2021-22. This opened opportunities for sustained mine life and production growth. All the Company’s deposits remain ‘open’ and the exploration team identified several new targets on mining leases having potential to increase R&R over the next 12 months. Across all the sites, we executed surface drilling of ~202 kilometres and ~298 kilometres of underground drilling, thereby totalling ~500 kilometres of drilling during the year. The extensive drilling supported in adding new Mineral Resources and upgrading Mineral Resource to Ore Reserves thereby sustaining Life of Mine of 10 years.</h4>
