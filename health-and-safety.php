                  <h3 class="head-font20 wow fadeInUp mar-t30">OCCUPATIONAL HEALTH MANAGEMENT</h3>
                  <p class="wow fadeInUp">Providing a safe working environment to every employee is an imperative at Hindustan Zinc. Employees safety includes prevention of occupational diseases and facilitates a hygienic working environment. Promotion of a healthier lifestyle increases productivity, reduces absenteeism, and enhances retention. We involve hi-tech processes and conduct lifestyle management trainings while regularly monitoring and controlling exposure to hazardous substances. </p>
