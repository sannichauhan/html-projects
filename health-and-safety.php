                  <h3 class="head-font20 wow fadeInUp mar-t30">INDUSTRIAL HYGIENE PROGRAMME AT HINDUSTAN ZINC</h3>
                  <p class="wow fadeInUp">The Company partnered with a globally recognised industrial hygiene service company, M/s International Safety Systems Inc. (ISS) to develop a sustainable industrial hygiene programme to reduce potential health risks by recognising, evaluating and controlling occupational health hazards and occupational exposures.</p>

                  <h3 class="head-font20 wow fadeInUp clr_sblue">THE JOURNEY FOCUSSED ON THE FOLLOWING KEY ELEMENTS</h3>
                  <div class="full-imgs mar-t30">
                    <img src="images/the-journey-focussed-on-img.svg" alt="" class="wow zoomIn">
                  </div>
                  <h4 class="head-font18 wow fadeInUp clr_sblue mar-t30">1. INDUSTRIAL HYGIENE CAPACITY BUILDING AND GOVERNANCE</h4>
                  <ul class="list-unstyled  all-ul-li">
                    <li class="wow fadeInUp">Leadership training on key aspects of industrial hygiene and occupational health</li> 

                    <li class="wow fadeInUp">Key professionals from EHS, medicine and process were identified to lead, champion and drive industrial hygiene programme at their respective locations</li> 

                    <li class="wow fadeInUp">Identified champions were nominated for certified industrial hygiene course and have currently completed 4 rigorous training modules on industrial hygiene from Occupational Hygiene Training Association (OHTA) and passed examinations from British Occupational Hygiene Society (BOHS). They will complete the remainder of two modules and then have an option to become internationally certified in industrial hygiene from BOHS </li>

                    <li class="wow fadeInUp">About 100 Hindustan Zinc process and maintenance employees completed on site training on specific hazards, engineering controls, and work practices </li>

                    <li class="wow fadeInUp">To drive the occupational health and industrial hygiene programme, the organisation has developed separate governance i.e., an industrial hygiene sub-committee is chaired by senior leaders and the name of the Corporate Safety Council was subsequently changed to Occupational Health and Safety Council. A similar structure was created at all zones</li>
                  </ul>

                  <h4 class="head-font18 wow fadeInUp clr_sblue">2. STANDARD DEVELOPMENTS</h4>
                  <ul class="list-unstyled  all-ul-li">
                    <li class="wow fadeInUp">Six standards on key elements of industrial hygiene and specific standards on lead and asbestos are developed. These standards were developed from globally recognised standards and guidelines </li>

                    <li class="wow fadeInUp">Each standard implementation process was developed and in field monitoring initiated</li>
                  </ul>
                  <h4 class="head-font18 wow fadeInUp clr_sblue">3. HAZARD CHARACTERISATION</h4>
                  <ul class="list-unstyled  all-ul-li">
                    <li class="wow fadeInUp">Hazard characterisation lies at the foundation of reducing potential occupational health risk. Hazards were characterised based on exposure limits, health hazard properties in terms of carcinogens, mutagens that can change genetic material, and agents causing reproductive hazards (CMR) </li>

                    <li class="wow fadeInUp">ISS characterised hazards for 500+ chemical agents across nine Hindustan Zinc sites</li>
                  </ul>

