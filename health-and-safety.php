                        <h4 class="head-font18 wow fadeInUp">SIMULATOR TRAINING</h4>
                        <p class="wow fadeInUp">To make mining safer and productive, simulators are used for training people for the operation of critical equipment like LPDT, LHD and jumbo drills. Training through simulators helps operators develop required skills and competencies. The simulated environment allows operators to test on specific equipment without using actual mobile equipment or exposing others to unnecessary hazards. We measure each operator’s reaction time against specific performance criteria, that helps us to assess training and development needs. The training programme begins with a pre-assessment of hand-eye co-ordination to determine each potential candidate’s capabilities. Once successful candidates have demonstrated the appropriate theoretical knowledge, they are trained on a simulator for the specific equipment they will be using and be licensed for. Next, candidates begin practice on actual equipment on-site, under the guidance of an experienced operator. Once they have had a successful assessment, they are licensed to operate the equipment.</p> 

