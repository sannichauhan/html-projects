        <h1>Zinc</h1>
        <h5>Market Review</h5>
        <img src="images/imgzincimg.png" class="img-responsive">

        <h6>GLOBAL DEMAND</h6>
        <p>Calendar year (CY) 2021 saw the COVID-19 pandemic persist, with several economies facing second or third waves with global imposition of lockdowns. Certain sectors like automobile and alloys were badly hit, although industrial activity was not impacted as much as in the first wave. Demand, however, started to pick up from the third quarter of the financial year. Led by expansion in manufacturing activity, zinc markets staged a robust recovery in CY 2021, with consumption growing by 7.1% to 14.15 Mt, one of the highest levels witnessed in the recent past.</p>

        <p>The second half of CY 2021 saw smelter disruption in Europe, with high energy prices and carbon emission taxes prompting some smelters to reduce output during times of peak power costs, leading to energy
        shortages. The energy crisis was further escalated by the Russia Ukraine war, as Europe is mostly dependent on Russia for energy. The disruption in the commodity market was particularly serious in the month of March 2022, causing the London Metal Exchange (LME) to intervene and halt nickel trading in view of extreme price volatility. This caused a cascading effect on LME prices and witnessed zinc prices touching an all time high of US$ 4,896/t.</p>

        <p>Zinc is mostly consumed by the construction sector, followed by transport and infrastructure sectors. In terms of first use, galvanising dominates, followed by fertiliser, die-casting alloys, brass semis, and casting. India has emerged as one of the stronger growth drivers in Zinc consumption across the world in post pandemic times. Demand in most of the first use applications has been restored back to pre-pandemic times. Further emerging areas of zinc-based application (with potential to replace lithium ion and lead-acid batteries) and renewable energy are likely to provide a push in the medium term. Renewable energy projects will provide an opportunity for use of galvanised steel in plant construction as well as transmission towers.</p>

        <h6>PRODUCTION </h6>
        <p>Global zinc mined metal production (metal in concentrate) grew by 7.3% to 13.16 Mt in CY 2021 and predicted to rise further in the medium term as new production comes up. Zinc refined metal production grew
          marginally to 13.84 Mt in CY 2021. Smelters, however, faced the challenge of high power costs and some even resorted to production cuts. Concentrate levels, which were impacted due to loss of mine production in CY 2020 and slipped into a deficit, showed an improvement of 1.06 Mt, and came back to a net supply
        surplus from Q3 CY 2021. It is expected to return to normalcy in CY 2022 and CY 2023. Global refined zinc metal inventory is also expected to normalise.</p>
        <h6>INDIA DEMAND AND OPPORTUNITY</h6>
        <p>India’s zinc consumption grew by approximately 13% in FY 2021-22 to 630 kt, after witnessing a sharp fall of 15% during COVID-affected FY 2020-21. While demand in the first quarter (April-June 2021) shrank by 20% due to the second wave of the pandemic, it eventually picked up from the second half of FY 2021-22, demonstrating a ‘V-shape’ recovery. Much of this growth came from the infrastructure sector and huge
        government projects announced in the second half of the year.</p>

        <p>The Indian government’s 5 trillion dollar economy vision, the Steel Ministry’s ambition to enhance India’s steel production capacity by 2.5x to 300 Mt, 5G roll-out and target for 100% electrification are likely to further boost zinc demand. The Central Government’s &#8377;6 lac crore asset monetisation pipeline during FY 2022-25 is likely to give further impetus to zinc demand. Funds sourced from these initiatives are proposed to be
        channelised towards roads, railways, power generation and transmission, and telecom projects among others. These projects require significant use of galvanised steel, which will aid zinc demand.</p>

        <p>There is also a huge potential for galvanised sheets and various zinc alloys in the government’s smart cities project, envisaged with modern real estate. Zinc, as an emerging technology, is also well poised for usage in battery application. Given the abundance of zinc in India, the metal is well-positioned to replace imported lithium in battery applications, and thus further promoting India’s self-reliance journey related to metal
        consumption.</p>
        <h6>GLOBAL OUTLOOK</h6>
        <p>As per WoodMac, global zinc consumption is likely to grow by 1.3% in CY 2022 and average 1.7% during CY 2023 and CY 2024, as governments across the world switch focus from job creation to de-carbonisation and infrastructure. Compared with zinc’s recent pre-pandemic history, this is a robust growth rate, sufficient to lift consumption to 14.5 Mt, thus surpassing the 2017 all-time high of 14.2 Mt.</p>

        <p>Majority of the growth is likely to be driven by investments in infrastructure and other forms of construction in developing regions, especially Asia, as opposed to the matured economies of developed nations. Demand in Asia is expected to grow at an average of 1.8% during this period. China is expected to remain the largest market, contributing to over 70% of the increased global zinc consumption, despite a sharp slowdown in its real estate construction.</p>


        <h6>BUSINESS OVERVIEW </h6>
        <p>Hindustan Zinc, with a primary zinc market share of 80% (including alloys) in FY 2021-22, remained India’s largest primary zinc producers. Our portfolio includes a range of zinc products including special high grade (SHG), high grade (HG), continuous galvanising grade (CGG), prime western (PW), jumbo SHG and other grades used in die-casting alloys. This makes us an attractive player in India, where over 70% of the demand comes from galvanising steel, used in construction and infrastructure sectors. Our SHG zinc products are registered with LME. We are working closely with customers to enhance the robustness of our zinc product portfolio in terms of value-added products (VAP).</p>

        <p>Hindustan Zinc, in collaboration with International Zinc Association (IZA), is working on multiple projects to increase zinc consumption in India. We are creating awareness on use of zinc as an alternative raw material in battery solutions. India’s current energy capacity of 60-65 GWh is expected to reach 600-700 GWh as per CRISIL, with zinc-based batteries touching 10-15%. We are in discussion with several experts working globally on technology options for zinc-based batteries.</p>

        <p>Our galvanised rebar segment is picking pace and expected to grow by 22.56% CAGR till 2030. We are working on increased consumer awareness alongside IZA. As our rail track galvanisation project is at an advanced stage we are witnessing a positive development, which can increase zinc consumption in the country. We are also working on a flagship initiative alongside IZA on zinc deficiency in soil.</p>


