              <h3 class="head-font20 wow fadeInUp clr_white">CASE STUDY</h3>
              <div class="curly-border">
                <h4 class="head-font20 wow fadeInUp">Vital Step on ESG Journey <span class="block-txt">
                USABLE JAROSITE PRODUCTION FOR CEMENT INDUSTRIES</span></h4>
                <p class="wow fadeInUp">In Zinc hydrometallurgy process, Jarosite is produced during the leaching process. It is further treated with lime and cement to produce Jarofix. Cement industries can use Jarosite as replacement for gypsum in their final clinker grinding process. For this purpose, the moisture content in the jarosite should be less than 30-35%. The Jarosite produced during our operations has a 50% moisture content that can traditionally be reduced through an expensive process of Filter press, which requires significant capex outflow and time for erection and commissioning.</p>
                <p class="wow fadeInUp">Our in-house team found a cost-effective method of using quick dry lime in the jarosite to bring it to the desired moisture levels. Later, the same method was successfully trialed at a cement plant. Going forward, the same practice will be continued on a sustainable basis as a permanent solution to gainful utilisation of waste.</p>
                <p class="clr_blue"><b>Benefits</b></p>
                <ul class="list-unstyled all-ul-li">
                  <li class="wow fadeInUp">Huge capex savings on estimated filter press installation</li>

                  <li class="wow fadeInUp">Annual savings on consumables and O&M</li>

                  <li class="wow fadeInUp">Annual capital cost saving on storage area development</li>

                  <li class="wow fadeInUp">Elimination of land requirements with fewer environmental and social concerns</li>

                </ul>
              </div>
              <h3 class="head-font20 wow fadeInUp  mar-t30">CASE STUDY</h3>
              <div class="curly-border">
                <h4 class="head-font20 wow fadeInUp">Marking another historic milestone <span class="block-txt">
                IMPROVING ZINC SMELTER DEBARI CELL HOUSE PRODUCTIVITY & EFFICIENCY</span></h4>
                <p class="wow fadeInUp">Improving productivity and efficiency levels of the Zinc Smelter Debari (ZSD) Cell House without suspending operations or compromising safety was no mean task. The challenge lay in
                  replacement of the cells without EOT cranes, in a running plant. A key area of priority was to ensure the safety of the people, the plant building and other plant structures. What aggravated the
                challenge was the completion of the tasks of handling, lifting, and shifting of approximately 8,000 MT material within four months.</p>

                <p class="wow fadeInUp">The team explored and worked out options to develop a method to mechanise the removal and replacement of cells. After two months of detailed deliberation, vendor selection and finalisation, it opted for the Mechanised Gantry System as a safe and suitable option for the replacement.
                </p>
                <p class="clr_blue"><b>The Revamp Project</b></p>
                <ul class="list-unstyled all-ul-li">
                  <li class="wow fadeInUp">Damaged concrete electrolytic cells were replaced with the latest poly concrete cells, having better shelf-life and efficiency levels, besides zero repair and maintenance requirements</li>

                  <li class="wow fadeInUp">The project, which commenced on June 8, 2021, was successfully completed on October 16, 2021</li>

                  <li class="wow fadeInUp">Smooth commissioning of cell house and completion of the revamp was achieved without any injury</li>

                </ul>
                

