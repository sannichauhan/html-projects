        <h6>GLOBAL DEMAND</h6>
        <p>Global silver demand rose by a healthy 19% last year, surpassing pre-pandemic volumes and achieving its highest level since 2015. Demand gained in all categories of silver, with the largest in volume gains being coin and bar purchases, followed by industrial demand. This trend reflects the recommencement of industrial operations and re-opening of businesses as economies began to recover from the COVID-19 pandemic. Other supportive factors included a boom in consumer electronics, 5G infrastructure investment, inventory build along the supply pipeline and rising end-use in the green economy, chiefly in photovoltaics.</p>
        <h6>PRODUCTION </h6>
        <p>In CY 2021, global mine production increased by 5.3% year-on-year (y-o-y). This was the biggest annual rise in silver output since CY 2013 and was largely the result of a recovery in production following significant
          disruption to mining during the COVID-19 pandemic in CY 2020. Output from primary silver mines increased the most, up 10.2% y-o-y, as these operations were disproportionately impacted by pandemic-related
        restrictions in CY 2021.</p>
        <h6>INDIA DEMAND AND OPPORTUNITY</h6>
        <p>India is currently the world’s third largest silver physical investment market after US and Germany. The silver bar market has been extremely successful, with around 15,000 tonnes of silver having been bought
        cumulatively over the last 10 years. The middle class Indian household regularly buys silver for investment and in the form of tableware and cutlery for household use. Silver purchase is considered a safety net for times of financial distress and hence, domestic consumption on the physical side is sizeable. Although new consumption themes are evolving and there is a growing popularity of e-commerce platforms including Amazon and Flipkart as an alternate destination for buying silver coins/bars, holding physical silver still involves space constraints and security concerns.</p>

        <p>The silver investment market is witnessing a slow change, with digital silver and silver ETFs both gaining popularity. The Securities and Exchange Board of India (SEBI) has allowed silver ETFs. Several mutual funds have launched silver ETFs, including Aditya Birla Sun Life, Nippon India and ICICI Prudential. All these factors together bode well for the silver investment market in India.</p>

        <h6>PRICES </h6>
        <p>During FY 2021-22, silver moved from trading within a US$ 24-28 range for most of the first half of last year to fluctuating within a US$ 22-26 band during the second half. Still, the full-year 2021 average achieved a 22% y-o-y increase to a nine-year high of US$ 25.14. Russia’s invasion of Ukraine in late February 2022, the conflict and its implications on trade and the world economy have become a principal driver of the global market. The price is expected to remain range bound in the US$ 21-US$ 25 band.</p>
        <p><i>*Source: World Silver Survey 2022</i></p>

