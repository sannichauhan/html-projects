                <h3 class="head-font20 wow fadeInUp">CODE OF CONDUCT</h3>
                <p class="wow fadeInUp">Strict compliance to the Company’s Code of Conduct is ensured at all levels of the organisational hierarchy, and also by the external stakeholders, including our partners, contractors, shareholders and other stakeholders. The code is rooted in the values of trust, mutual respect, professionalism, responsibility, accountability and transparency in communication. We have strategically aligned the Code with the Foreign Corrupt Practices Act and UK Bribery Act, 2010. Besides guiding our behaviour and actions, the Code facilitates our efforts to promote honest and ethical conduct across the Company. It also ensures ethical handling of any conflicts of interest, complete and timely disclosures, among others.</p>
                <h4 class="clr_sblue head-font18">STRICT ADHERENCE TO THE CODE</h4>
                <p class="wow fadeInUp">All the Board members, executives and new employees affirm their adherence to the Code on an annual basis. We have instituted an Ethics Committee to orient and address the principles and standards contained in the Code. </p>

                <p class="wow fadeInUp">We had, in FY 2021-22, strengthened the Code to give better indication of its complete applicability and clearer guidance on Facilitation Payments, Gifts and Business Partner relations. We had also introduced several contemporary concepts for Sanctions & Trade Controls and Social Media conduct, besides expanding the ambit of confidentiality with Data Privacy & GDPR and Information security. </p>

                <p class="wow fadeInUp">In line with our commitment to diversity and inclusion, we had incorporated these elements, along with equal opportunity, in our Code, and also made our POSH policy gender-neutral. Inclusion of Charity & Sponsorships along with an Acknowledgement to the Code, as well as a separate annexure for Antibribery & Anti-corruption Policy, were some of the other new features added in the previous fiscal.</p>
                <h3 class="head-font20 wow fadeInUp">WHISTLE-BLOWER POLICY</h3>
                <p class="wow fadeInUp">Our Whistle-Blower Policy actively encourages and empowers our employees to raise concerns about any unfair business dealing in the organisation. The responsibility for addressing the complaints vests with the Group Head – Management Assurance. The complaint registration process is simple and can be completed via a mobile application in just three clicks. After in-depth investigation, the report for complaint resolution is shared with the Audit Committee for review. We have also established a special ‘Whistle-Blower Portal for Safety’ to encourage compliance with safety practices.</p>
                <h3 class="head-font20 wow fadeInUp">ANTI-BRIBERY AND ANTI-CORRUPTION</h3>
                <p class="wow fadeInUp">Compliance with the anti-bribery and anti-corruption policies is strictly ensured at Hindustan Zinc for ensuring fair and transparent conduct of all business transactions. All our employees and Business Partners are trained and apprised about the principles of our Code of Conduct as well as the importance of strict adherence. Undue monetary or facilitation payment to any person or persons, including public officials, customers, or to any other organisation, is strictly prohibited and checked.</p>
