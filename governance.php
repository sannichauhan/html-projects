                <h3 class="head-font20 wow fadeInUp mb-mar-t30">EXECUTIVE COMMITTEE</h3>
                <p class="wow fadeInUp">The Executive Committee acts as the bridge between the Company and the Board. It comprises Hindustan Zinc’s functional and plant heads, including the Chief Executive Officer, the Interim Chief Financial Officer and the IBU CEOs. It oversees the management and control functions at the Company and plays a pivotal role in the execution of its strategies. It is responsible for coordinating the various business transactions, besides steering process improvements at mines and smelters.</p>
                <h3 class="head-font20 wow fadeInUp">IBUs FOR EXECUTION</h3>
                <p class="wow fadeInUp">To ensure effective execution of our strategies, plans and programmes, we have set up Independent Business Units (IBUs), with a well-articulated strategy to drive their actions. Sustainability is embedded in all our business decisions and the IBUs work concertedly towards the realisation of our sustainability goals.</p>
