        <h1>External Environment</h1>
        <h6>GLOBAL ECONOMY GROWS AS BUSINESSES RECOVER FROM PANDEMIC-RELATED SHOCKS</h6>
        <p>The global economy staged a robust recovery in calendar year (CY) 2021 as vaccination drives were
        conducted on a war-footing by governments across the world which led to reduced disease transmission. The world adapted to the new normal, which led to resumption of economic activities and unblocking of supply chain. As per the International Monetary Fund, global GDP grew by 6.1% as against a decline of 3.1% during the pandemic-hit CY 2020, which adversely impacted economic activity.</p>

        <p>The outlook for the global economy in CY 2022 remains moderate with a 3.6% growth amidst increasing Omicron variant caseloads, rising input prices and wider implications of Ukraine-Russia war. The war led to a surge in crude oil prices to US$ 124 per barrel (bbl) in the first week of March 2022 and is predicted to
        average US$ 103/bbl in CY 2022 as per the estimates of the US Energy Information Administration (EIA). In FY 2021-22, coal prices witnessed high volatility with Newcastle Index basis 6000 Net Calorific Value Kcal/kg (ARB) of averaging at US$ 181/t for the fiscal year touching a peak of US$ 440/t on March 3, 2022 and closed the year at a high of US$ 326/t as on March 31, 2022.</p>

        <p>Indian economy too faced stiff challenges during the second wave of COVID-19 around April-May of CY 2021. However, since then rapid vaccinations, opening up of mobility, enhanced economic activity and upbeat consumer sentiments have led to a positive trajectory. As per the National Statistics Office, India’s GDP grew by 8.7% in FY 2021-22 as against a decline of 6.6% in FY 2020-21.</p>
        <h6>INDIAN GOVERNMENT’S RENEWED FOCUS ON FUELLING GROWTH</h6>
        <p>The Indian Government infused around US$ 300 billion in the economy in response to the pandemic along with numerous tax, employment, healthcare, social security, credit and other policy level measures. The Lok Sabha and Rajya Sabha passed the Mines and Minerals (Development and Regulation) Amendment Bill, 2021, which aims to reform the mining industry with the intent to eliminate the distinction between captive and non-captive mines. Under the Bill, captive mines would be allowed to sell up to 50% of the minerals
        excavated. The Bill also permits the Central Government to conduct auctions for those blocks where State Governments face challenges or fail to conduct it.</p>
        <h6>ADAPTING TO EVOLVING METAL SECTOR DYNAMICS <span class="pdtop10">INCREASING PER CAPITA CONSUMPTION OF ZINC</span></h6>
        <p>Hindustan Zinc in collaboration with International Zinc Association (IZA) is working on multiple projects to increase zinc consumption in India. We are creating awareness on use of zinc as an alternate raw material in battery solutions and are in discussion with several experts globally on the availability of technology options for zinc-based batteries. Our galvanised rebar segment is also picking pace and we are working towards increasing consumer awareness alongside IZA. Additionally, our rail track galvanisation initiative is in an
        advanced stage and we remain optimistic of progress on this front from the Indian Railways.</p>

        <p>Another flagship initiative alongside IZA is to address zinc related deficiencies in agricultural soil. Towards this end, we have recently signed a Memorandum of Understanding (MoU) with Maharana Pratap University, Udaipur, a leading university in the Indian state of Rajasthan. The intent is to create awareness amongst the farming community on zinc fortification through field demonstration and capability building.
        </p>
